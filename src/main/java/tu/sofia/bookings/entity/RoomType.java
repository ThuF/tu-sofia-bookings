/**
 * Enumeration of all types of rooms
 */
public enum RoomType {

	/**
	 * The standard room type
	 */
	STANDARD,

	/**
	 * The moderate room type
	 */
	MODERATE,

	/**
	 * The superior room type
	 */
	SUPERIOR,

	/**
	 * The deluxe room type
	 */
	DELUXE,

	/**
	 * The run of house room type
	 */
	RUN_OF_HOUSE,

	/**
	 * The junior suite room type
	 */
	JUNIOR_SUITE,

	/**
	 * The suite room type
	 */
	SUITE,

	/**
	 * The studio room type
	 */
	STUDIO
}
